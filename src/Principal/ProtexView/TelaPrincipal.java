    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
            
        DespezaDTO objDespeza = new DespezaDTO();
        
        
        objDespeza.setDespeza(txtDespeza.getText());
        
       double valorGasto = Double.parseDouble(txtValorGasto.getText());
       java.text.NumberFormat nf = java.text.NumberFormat.getNumberInstance();
       nf.setMinimumFractionDigits(2);
       nf.setMaximumFractionDigits(2);
       String str = nf.format(valorGasto);
       str = str.replaceAll(",",".");
       objDespeza.setValorGasto(Double.parseDouble(str));
        
        objDespeza.setDescricao(txtDescricao.getText());
        
        DespezaDAO objDespezaDAO = new DespezaDAO();
        
        
        boolean cad = objDespezaDAO.registrar(objDespeza);
        if(cad){
            JOptionPane.showMessageDialog(null, "Despeza cadastrada com sucesso!");
            
            txtDespeza.setText("");
            txtDescricao.setText("");
            txtValorGasto.setText("");
            PesquisarTable();
        } else {
            JOptionPane.showMessageDialog(null, "Falha ao cadastrar despeza");
        }
       
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
           if (JTDespezas.getSelectedRow() !=-1){
            
                DespezaDTO objDespeza = new DespezaDTO();
                objDespeza.setDespeza(txtDespeza.getText());

                double valorGasto = Double.parseDouble(txtValorGasto.getText());
                java.text.NumberFormat nf = java.text.NumberFormat.getNumberInstance();
                nf.setMinimumFractionDigits(2);
                nf.setMaximumFractionDigits(2);
                String str = nf.format(valorGasto);
                str = str.replaceAll(",",".");
                objDespeza.setValorGasto(Double.parseDouble(str));

                objDespeza.setDescricao(txtDescricao.getText());
                objDespeza.setId((int)JTDespezas.getValueAt(JTDespezas.getSelectedRow(), 0));

                DespezaDAO objDespezaDAO = new DespezaDAO();
                boolean cad = objDespezaDAO.editar(objDespeza);
                if(cad){
                    JOptionPane.showMessageDialog(null, "Despeza editada com sucesso!");
                    txtDespeza.setText("");
                    txtDescricao.setText("");
                    txtValorGasto.setText("");
                    PesquisarTable();
                } else {
                    JOptionPane.showMessageDialog(null, "Falha ao editar despeza");
                }
               
        } 
    }//GEN-LAST:event_btnEditarActionPerformed

