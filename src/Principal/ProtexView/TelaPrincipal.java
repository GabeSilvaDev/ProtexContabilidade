    public void PesquisarTable(){
        DefaultTableModel modelo = (DefaultTableModel) JTDespezas.getModel();
        modelo.setNumRows(0);
        DespezaDAO ddao = new DespezaDAO();
        
        for(DespezaDTO d: ddao.Pesquisar()){
            
            modelo.addRow(new Object[]{
                d.getId(),               
                d.getDespeza(),
                d.getDescricao(),
                d.getValorGasto()
            });
        }
    }
    
    public void PesquisarTableDesc(String desc){
        DefaultTableModel modelo = (DefaultTableModel) JTDespezas.getModel();
        modelo.setNumRows(0);
        DespezaDAO ddao = new DespezaDAO();
        
        for(DespezaDTO d: ddao.PesquisarDesc(desc)){
            
            modelo.addRow(new Object[]{
                d.getId(),
                d.getDespeza(),
                d.getDescricao(),
                d.getValorGasto()
            });
        }
    }
